
package modelo;


public class ModeloContas {
    
    private String NumeroCasa;
    private String ContasPagas;
    private String ContasApagar;

    /**
     * @return the NumeroCasa
     */
    public String getNumeroCasa() {
        return NumeroCasa;
    }

    /**
     * @param NumeroCasa the NumeroCasa to set
     */
    public void setNumeroCasa(String NumeroCasa) {
        this.NumeroCasa = NumeroCasa;
    }

    /**
     * @return the ContasPagas
     */
    public String getContasPagas() {
        return ContasPagas;
    }

    /**
     * @param ContasPagas the ContasPagas to set
     */
    public void setContasPagas(String ContasPagas) {
        this.ContasPagas = ContasPagas;
    }

    /**
     * @return the ContasApagar
     */
    public String getContasApagar() {
        return ContasApagar;
    }

    /**
     * @param ContasApagar the ContasApagar to set
     */
    public void setContasApagar(String ContasApagar) {
        this.ContasApagar = ContasApagar;
    }
    
    
}
