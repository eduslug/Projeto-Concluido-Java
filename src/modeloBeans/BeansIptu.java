
package modeloBeans;


public class BeansIptu {
    
    private String ano;
    private String casa;
    private String valor;
    private String pesqiusas;
    private int codigo;
    /**
     * @return the ano
     */
    public String getAno() {
        return ano;
    }

    /**
     * @param ano the ano to set
     */
    public void setAno(String ano) {
        this.ano = ano;
    }

    /**
     * @return the casa
     */
    public String getCasa() {
        return casa;
    }

    /**
     * @param casa the casa to set
     */
    public void setCasa(String casa) {
        this.casa = casa;
    }

    /**
     * @return the valor
     */
    public String getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(String valor) {
        this.valor = valor;
    }

    /**
     * @return the pesqiusas
     */
    public String getPesqiusas() {
        return pesqiusas;
    }

    /**
     * @param pesqiusas the pesqiusas to set
     */
    public void setPesqiusas(String pesqiusas) {
        this.pesqiusas = pesqiusas;
    }

    /**
     * @return the codigo
     */
    public int getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

  
    
}
